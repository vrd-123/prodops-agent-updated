def generate_fhir_cleanup_script(params: FHIRCleanupParams, config: dict | None = None) -> str:
    """
    Generate FHIR cleanup script(s).
    For multi-env tickets, generates separate script blocks per environment.
    """
    if config is None:
        config = load_env_config()

    customer_cfg = resolve_customer(params.customer, config)
    ts = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")

    if not customer_cfg:
        return _error_block(
            params.ticket_key,
            f"Customer '{params.customer}' not found in env_config.yaml.",
        )

    if not params.is_valid:
        return _missing_fields_block(params.ticket_key, params.missing_fields)

    aws_profile = customer_cfg["aws_profile"]

    # Build header with warnings
    header_lines = []
    for warn in params.warnings:
        header_lines.append(warn)
    header = "\n".join(header_lines) + "\n\n" if header_lines else ""

    # Generate a script block for each FHIR environment
    script_blocks = []
    for fhir_env in params.fhir_environments:
        block = _generate_single_fhir_block(
            params=params,
            fhir_env=fhir_env,
            aws_profile=aws_profile,
            ts=ts,
        )
        script_blocks.append(block)

    return header + "\n\n".join(script_blocks)


def _generate_single_fhir_block(
    params: FHIRCleanupParams,
    fhir_env: str,
    aws_profile: str,
    ts: str,
) -> str:
    """Generate script for a single FHIR environment."""

    is_prod = "prd" in fhir_env or "prod" in fhir_env
    catalog = params.get_catalog(fhir_env)
    env_tables = params.get_tables_for_env(fhir_env)

    # Build filter flags
    filter_flags = []
    for rt in params.resource_types:
        filter_flags.append(f"  -f {rt}")
    for profile in params.profile_urls:
        filter_flags.append(f'  -f "{profile}"')
    filter_str = " \\\n".join(filter_flags) if filter_flags else "  # ⚠️ No resource filters found — add -f flags manually"

    lines = []

    # Header
    if len(params.fhir_environments) > 1:
        lines.append(f"# ╔{'═' * 54}╗")
        lines.append(f"# ║  ENVIRONMENT: {fhir_env:<39}║")
        lines.append(f"# ╚{'═' * 54}╝")
        lines.append("")

    lines.append("#!/bin/bash")
    lines.append(f"# {'=' * 68}")
    lines.append(f"# {params.ticket_key}: {params.summary[:60]}")
    lines.append(f"# Generated by ProdOps Bot — {ts}")
    lines.append(f"# Customer: {params.customer} | Environment: {fhir_env}")
    if catalog:
        lines.append(f"# Databricks catalog: {catalog}")

    # NEW: Ticket context
    lines.append(f"# ---")
    lines.append(f"# Resources: {', '.join(params.resource_types) if params.resource_types else 'see profile URLs below'}")
    if params.metadata_tables:
        lines.append(f"# Metadata tables: {', '.join(t.split('.')[-1] for t in params.metadata_tables[:4])}")
    lines.append(f"# ---")

    if is_prod:
        lines.append(f"# ⚠️  PRODUCTION ENVIRONMENT — --force-prod required")
    lines.append(f"# ⚠️  REVIEW EVERY STEP BEFORE EXECUTING")
    lines.append(f"# {'=' * 68}")
    lines.append("")

    # Prerequisites
    lines.append("# === PREREQUISITES ===")
    lines.append("# ✅ JIT access activated for this tenant")
    lines.append("# ✅ fhir_cleanup.py + fhir_environments.yaml downloaded")
    lines.append("# ✅ Python venv with pyyaml>=6.0")
    lines.append("")

    # Step 1: AWS SSO Login
    lines.append("# === STEP 1: AWS SSO Login ===")
    lines.append(f"aws sso login --profile {aws_profile}")
    lines.append("")

    # Step 2: Dry run
    lines.append("# === STEP 2: DRY RUN — MANDATORY ===")
    lines.append(f"python fhir_cleanup.py \\")
    lines.append(f"  --config ./fhir_environments.yaml \\")
    lines.append(f"  -e {fhir_env} \\")
    lines.append(f"{filter_str} \\")
    lines.append(f"  --dry-run")
    lines.append("")

    # Step 3: Databricks metadata check
    if env_tables:
        lines.append("# === STEP 3: Verify counts in Databricks SQL ===")
        count_parts = []
        for t in env_tables:
            table_ref = t if "." in t and "_catalog" in t else f"{catalog}.{t}" if catalog else t
            short_name = t.split(".")[-1]
            count_parts.append(f"SELECT '{short_name}' AS tbl, COUNT(*) AS cnt FROM {table_ref}")
        count_query = "\nUNION ALL\n".join(count_parts)
        lines.append(f"# {count_query};")
        lines.append("")
    else:
        lines.append("# === STEP 3: (No metadata tables specified — skip) ===")
        lines.append("")

    # Step 4: Execute
    lines.append('echo "👆 Review dry-run counts. Proceed only if they match expectations."')
    lines.append("")
    lines.append("# === STEP 4: EXECUTE — only after dry-run verification ===")
    lines.append(f"python fhir_cleanup.py \\")
    lines.append(f"  --config ./fhir_environments.yaml \\")
    lines.append(f"  -e {fhir_env} \\")
    lines.append(f"{filter_str} \\")
    if is_prod:
        lines.append(f"  --execute --force-prod")
    else:
        lines.append(f"  --execute")
    lines.append("")

    # Step 5: Metadata cleanup
    if env_tables:
        lines.append("# === STEP 5: Databricks metadata cleanup (AFTER MongoDB delete confirmed) ===")
        for t in env_tables:
            table_ref = t if "." in t and "_catalog" in t else f"{catalog}.{t}" if catalog else t
            lines.append(f"# DELETE FROM {table_ref};")
        lines.append("")
        lines.append("# Verify 0 records after delete:")
        for t in env_tables:
            table_ref = t if "." in t and "_catalog" in t else f"{catalog}.{t}" if catalog else t
            lines.append(f"# SELECT COUNT(*) FROM {table_ref};")
        lines.append("")

    # FHIR server URLs
    if params.fhir_server_urls:
        lines.append("# === FHIR Server URLs (for manual verification) ===")
        for url in params.fhir_server_urls:
            lines.append(f"# {url}")
        lines.append("")

    # Troubleshooting
    lines.append("# === TROUBLESHOOTING ===")
    lines.append("# 'Environment not allowlisted' → Add to fhir_environments.yaml")
    lines.append("# 'AWS credentials not available' → Run aws sso login")
    lines.append("# 'No running MongoDB pods' → Check EKS context + namespace")
    if is_prod:
        lines.append("# 'Environment is marked prod' → Must use --force-prod")
    lines.append("# Count mismatch → STOP. Contact ticket reporter before deleting")

    return "\n".join(lines)


# ============================================================================
# Error / Missing Field Blocks
# ============================================================================

def _error_block(ticket_key: str, message: str) -> str:
    return (
        f"⚠️ *Script Generation Failed for {ticket_key}*\n\n"
        f"```\n{message}\n```\n\n"
        f"Check env_config.yaml and ensure the customer is configured."
    )


def _missing_fields_block(ticket_key: str, missing: list[str]) -> str:
    field_list = "\n".join(f"  • {f}" for f in missing)
    return (
        f"⚠️ *Cannot generate script for {ticket_key}* — missing required info:\n\n"
        f"{field_list}\n\n"
        f"Provide the missing details when requesting the script."
    )
