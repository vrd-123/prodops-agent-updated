"""
ProdOps Phase 2 — Script Generator (v2.2)
===========================================
Takes parsed ticket parameters + env_config and generates
complete, configured, copy-paste-ready shell scripts.

v2.2 Changes:
  - FIX: Pod name no longer doubles the "prodops" prefix
  - NEW: Pre-check step validates destination paths exist before copy
  - NEW: Ticket context comment shows source text for file list
  - NEW: Cross-reference step checks past tickets for pattern validation
  - NEW: Known BlueKC dev path pattern documented (bluekc-stg/ is intentional)

Built from real runbooks:
  S3 Copy:      https://abacusinsights.atlassian.net/wiki/spaces/ES/pages/5629607976
  FHIR Cleanup: https://abacusinsights.atlassian.net/wiki/spaces/ES/pages/5836767241
"""

import os
import yaml
import logging
from datetime import datetime, timezone

from phase2.parsers.ticket_parser import S3CopyParams, FHIRCleanupParams

logger = logging.getLogger(__name__)

_env_config: dict | None = None


def load_env_config(config_path: str | None = None) -> dict:
    """Load and cache env_config.yaml."""
    global _env_config
    if _env_config is not None:
        return _env_config
    if config_path is None:
        config_path = os.path.join(
            os.path.dirname(os.path.dirname(__file__)),
            "config", "env_config.yaml",
        )
    with open(config_path, "r") as f:
        _env_config = yaml.safe_load(f)
    return _env_config


def resolve_customer(customer_name: str, config: dict) -> dict | None:
    """Resolve customer name to infra details."""
    customers = config.get("customers", {})

    if customer_name in customers:
        return customers[customer_name]

    for key, val in customers.items():
        if key.lower() == customer_name.lower():
            return val

    aliases = config.get("customer_aliases", {})
    for alias, canonical in aliases.items():
        if alias.lower() == customer_name.lower():
            return customers.get(canonical)

    return None


